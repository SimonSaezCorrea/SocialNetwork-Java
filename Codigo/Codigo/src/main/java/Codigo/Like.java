package Codigo;

import java.util.Date;

/**
 *
 * @author Equipo
 */
public class Like {
    int id;
    Date date;
    User author;

    public Like(int id, Date date, User author) {
        this.id = id;
        this.date = date;
        this.author = author;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public User getAuthor() {
        return author;
    }

    public void setAuthor(User author) {
        this.author = author;
    }
    
    
}
